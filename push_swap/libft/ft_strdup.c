#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*s1;
	int		i;

	i = 0;
	s1 = (char *)malloc(sizeof(char) * ft_strlen(s) + 1);
	if (!s1)
		return (0);
	while (s[i])
	{
		s1[i] = s[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
